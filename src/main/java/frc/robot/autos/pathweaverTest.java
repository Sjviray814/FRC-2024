public class pathweaverTest extends SequentialCommandGroup {
    Trajectory trajectory = new Trajectory();
    public pathweaverTest(Swerve s_Swerve){
        
        TrajectoryConfig config =
            new TrajectoryConfig(
                    Constants.AutoConstants.kMaxSpeedMetersPerSecond,
                    Constants.AutoConstants.kMaxAccelerationMetersPerSecondSquared)
                .setKinematics(Constants.Swerve.swerveKinematics);

        String trajectoryFilePath = "output/test.wpilib.json";
        

        try{
            Path path = Filesystem.getDeployDirectory().toPath().resolve(trajectoryFilePath);
            trajectory = TrajectoryUtil.fromPathweaverJson(path);
        }
        catch(IOException ex){
            DriverStation.reportError("Unable to open trajectory: " + trajectoryFilePath, ex.getStackTrace());
        }
        

        var thetaController =
            new ProfiledPIDController(
                Constants.AutoConstants.kPThetaController, 0, 0, Constants.AutoConstants.kThetaControllerConstraints);
        thetaController.enableContinuousInput(-Math.PI, Math.PI);

        SwerveControllerCommand swerveControllerCommand =
            new SwerveControllerCommand(
                trajectory,
                s_Swerve::getPose,
                Constants.Swerve.swerveKinematics,
                new PIDController(Constants.AutoConstants.kPXController, 0, 0),
                new PIDController(Constants.AutoConstants.kPYController, 0, 0),
                thetaController,
                s_Swerve::setModuleStates,
                s_Swerve);

        System.out.println(trajectory);
        addCommands(
            new InstantCommand(() -> s_Swerve.resetModulesToAbsolute()),
            new InstantCommand(() -> s_Swerve.setOdometry(trajectory.getInitialPose())),
            swerveControllerCommand
        );
    }
}